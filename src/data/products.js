// Helper functions - Enhanced to support new categories
export const getProductsByCategory = (category) => {
  if (category === 'all') return allProducts;
  return allProducts.filter(product => product.category === category);
};

export const getProductsByGender = (gender) => {
  if (gender === 'all') return allProducts;
  return allProducts.filter(product => {
    // Check explicit gender property first
    if (product.gender) return product.gender === gender;
    
    // Fallback to category-based gender detection
    if (gender === 'women') return product.category === 'women';
    if (gender === 'men') return !['women', 'tech'].includes(product.category);
    return true;
  });
};

export const getFeaturedProducts = () => {
  return allProducts.filter(product => product.featured);
};

export const getProductById = (id) => {
  return allProducts.find(product => product.id === parseInt(id));
};

export const searchProducts = (query) => {
  return allProducts.filter(product => 
    product.name.toLowerCase().includes(query.toLowerCase()) ||
    product.description.toLowerCase().includes(query.toLowerCase())
  );
};

export const getProductsByPriceRange = (min, max) => {
  return allProducts.filter(product => product.price >= min && product.price <= max);
};

export const getProductsByBadge = (badge) => {
  return allProducts.filter(product => product.badge?.includes(badge));
};

